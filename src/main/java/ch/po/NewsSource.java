package ch.po;

public class NewsSource {
	private String list_url;
	private String pre_url;
	private String list_rule;
	private String title_rule;
	private String content_rule;
	public String getList_url() {
		return list_url;
	}
	public void setList_url(String list_url) {
		this.list_url = list_url;
	}
	public String getPre_url() {
		return pre_url;
	}
	public void setPre_url(String pre_url) {
		this.pre_url = pre_url;
	}
	public String getList_rule() {
		return list_rule;
	}
	public void setList_rule(String list_rule) {
		this.list_rule = list_rule;
	}
	public String getTitle_rule() {
		return title_rule;
	}
	public void setTitle_rule(String title_rule) {
		this.title_rule = title_rule;
	}
	public String getContent_rule() {
		return content_rule;
	}
	public void setContent_rule(String content_rule) {
		this.content_rule = content_rule;
	}
}
